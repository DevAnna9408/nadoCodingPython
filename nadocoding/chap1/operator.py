# 연산자

print(1 + 1)
print(2 * 3)
print(10 / 2)

# 제곱은 곱하기 연산을 두 번 하면 된다.
print(2**3)

# 몫만 구하고자 할 때는 다른 언어와 다르게 슬러시를 두 번 써야 한다.
print(10//2)

# 나누기는 퍼센트를 한 번만 쓰면 된다.
print(10%2)

# 두 가지 이상의 연산을 할때는 and, or, &, | 를 쓰면 된다.
print((3 > 0) and (3 < 5))
print((3 > 0) or (3 < 5))
print((3 > 0) & (3 < 5))
print((3 > 0) | (3 < 5))

